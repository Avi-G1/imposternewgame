// Group words by category
export const wordCategories: WordCategory[] = (() => {
    const categoryMap = new Map<string, WordWithHint[]>();

    allWords.forEach(word => {
        if (!categoryMap.has(word.category)) {
            categoryMap.set(word.category, []);
        }
        categoryMap.get(word.category)!.push(word);
    });

    return Array.from(categoryMap.entries()).map(([name, words]) => ({
        name,
        words
    }));
})();

export function getRandomWord(category: WordCategory): string {
    const randomIndex = Math.floor(Math.random() * category.words.length);
    return category.words[randomIndex].word;
}

export function getWordWithHint(category: WordCategory, word: string): WordWithHint | undefined {
    return category.words.find(w => w.word === word);
}
